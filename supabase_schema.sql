-- ─── 6. Client Users (end-users of client bots) ───
CREATE TABLE IF NOT EXISTS client_users (
  id              BIGSERIAL PRIMARY KEY,
  client_id       BIGINT NOT NULL REFERENCES clients(id) ON DELETE CASCADE,
  telegram_id     TEXT NOT NULL,
  name            TEXT,
  context_summary TEXT,                        -- AI-generated summary of this user
  first_seen      TIMESTAMPTZ DEFAULT now(),
  last_seen       TIMESTAMPTZ DEFAULT now(),
  metadata        JSONB DEFAULT '{}',
  UNIQUE(client_id, telegram_id)
);

CREATE INDEX idx_client_users_lookup ON client_users(client_id, telegram_id);

-- ─── 7. Billing ───
CREATE TABLE IF NOT EXISTS billing (
  id              BIGSERIAL PRIMARY KEY,
  client_id       BIGINT NOT NULL REFERENCES clients(id) ON DELETE CASCADE,
  plan            TEXT NOT NULL,
  amount_rub      INTEGER NOT NULL,
  period_start    DATE NOT NULL,
  period_end      DATE NOT NULL,
  status          TEXT DEFAULT 'pending'
                  CHECK (status IN ('pending', 'paid', 'overdue', 'cancelled')),
  paid_at         TIMESTAMPTZ,
  invoice_url     TEXT,
  created_at      TIMESTAMPTZ DEFAULT now()
);

CREATE INDEX idx_billing_client ON billing(client_id);
CREATE INDEX idx_billing_status ON billing(status);

-- ─── 8. Onboarding Sessions (Dmitry's conversations) ───
CREATE TABLE IF NOT EXISTS onboarding_sessions (
  id                    BIGSERIAL PRIMARY KEY,
  prospect_telegram_id  TEXT NOT NULL,
  prospect_name         TEXT,
  stage                 TEXT DEFAULT 'intro'
                        CHECK (stage IN ('intro', 'questions', 'prompt_gen', 'bot_assign', 'testing', 'complete', 'abandoned')),
  collected_data        JSONB DEFAULT '{}',
  generated_prompt      TEXT,
  assigned_bot_id       BIGINT REFERENCES bot_pool(id),
  created_at            TIMESTAMPTZ DEFAULT now(),
  completed_at          TIMESTAMPTZ,
  metadata              JSONB DEFAULT '{}'
);

CREATE INDEX idx_onboarding_prospect ON onboarding_sessions(prospect_telegram_id);
CREATE INDEX idx_onboarding_stage ON onboarding_sessions(stage);

-- ─── 9. Error Log (monitoring) ───
CREATE TABLE IF NOT EXISTS error_log (
  id              BIGSERIAL PRIMARY KEY,
  client_id       BIGINT,
  bot_token_last4 TEXT,
  error_type      TEXT NOT NULL,               -- claude_error, telegram_error, db_error
  error_message   TEXT NOT NULL,
  context         JSONB DEFAULT '{}',
  created_at      TIMESTAMPTZ DEFAULT now()

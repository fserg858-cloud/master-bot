-- ─── 4. Usage (monthly counters) ───
CREATE TABLE IF NOT EXISTS usage (
  id                  BIGSERIAL PRIMARY KEY,
  client_id           BIGINT NOT NULL REFERENCES clients(id) ON DELETE CASCADE,
  month               TEXT NOT NULL,           -- "2026-04"
  requests_used       INTEGER DEFAULT 0,
  requests_limit      INTEGER NOT NULL,
  is_trial            BOOLEAN DEFAULT false,
  trial_requests_left INTEGER,
  created_at          TIMESTAMPTZ DEFAULT now(),
  UNIQUE(client_id, month)
);

CREATE INDEX idx_usage_client_month ON usage(client_id, month);

-- ─── 5. Conversations (all bot messages) ───
CREATE TABLE IF NOT EXISTS conversations (
  id                    BIGSERIAL PRIMARY KEY,
  client_id             BIGINT NOT NULL REFERENCES clients(id) ON DELETE CASCADE,
  end_user_telegram_id  TEXT NOT NULL,
  end_user_name         TEXT,
  role                  TEXT NOT NULL CHECK (role IN ('user', 'assistant')),
  content               TEXT NOT NULL,
  tokens_used           INTEGER DEFAULT 0,
  created_at            TIMESTAMPTZ DEFAULT now()
);

CREATE INDEX idx_conv_client_user ON conversations(client_id, end_user_telegram_id);
CREATE INDEX idx_conv_created ON conversations(created_at DESC);


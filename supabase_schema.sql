CREATE INDEX idx_errors_type ON error_log(error_type);
CREATE INDEX idx_errors_created ON error_log(created_at DESC);

-- ─── 10. Useful views ───

-- Active clients with their bot info
CREATE OR REPLACE VIEW v_active_clients AS
SELECT
  c.id,
  c.business_name,
  c.industry,
  c.plan,
  c.status,
  b.bot_username,
  c.onboarded_at,
  COALESCE(u.requests_used, 0) as current_month_requests
FROM clients c
LEFT JOIN bot_pool b ON b.id = c.bot_id
LEFT JOIN usage u ON u.client_id = c.id
  AND u.month = to_char(now(), 'YYYY-MM')
WHERE c.status IN ('trial', 'active')
ORDER BY c.onboarded_at DESC;

-- Daily stats
CREATE OR REPLACE VIEW v_daily_stats AS
SELECT
  date_trunc('day', created_at) AS day,
  COUNT(*) FILTER (WHERE role = 'user') AS user_messages,
  COUNT(*) FILTER (WHERE role = 'assistant') AS bot_messages,
  COUNT(DISTINCT end_user_telegram_id) AS unique_users,
  COUNT(DISTINCT client_id) AS active_bots,
  SUM(tokens_used) AS total_tokens
FROM conversations
WHERE created_at > now() - interval '30 days'
GROUP BY 1
ORDER BY 1 DESC;


-- ─── 1. Bot Pool (pre-created Telegram bots) ───
CREATE TABLE IF NOT EXISTS bot_pool (
  id            BIGSERIAL PRIMARY KEY,
  bot_username  TEXT NOT NULL UNIQUE,          -- @sergeev_agents_001_bot
  bot_token     TEXT NOT NULL UNIQUE,          -- from BotFather
  status        TEXT NOT NULL DEFAULT 'available'  -- available | assigned | suspended
                CHECK (status IN ('available', 'assigned', 'suspended')),
  assigned_to_client_id BIGINT,               -- FK filled on assignment
  created_at    TIMESTAMPTZ DEFAULT now(),
  assigned_at   TIMESTAMPTZ
);

CREATE INDEX idx_bot_pool_status ON bot_pool(status);
CREATE INDEX idx_bot_pool_token ON bot_pool(bot_token);
CREATE INDEX idx_bot_pool_client ON bot_pool(assigned_to_client_id);

-- ─── 2. Clients ───
CREATE TABLE IF NOT EXISTS clients (
  id              BIGSERIAL PRIMARY KEY,
  business_name   TEXT NOT NULL,
  industry        TEXT,                        -- "автомасла", "барбершоп", "юрфирма"
  contacts        JSONB DEFAULT '{}',          -- {phone, address, website, hours}
  system_prompt   TEXT NOT NULL,               -- generated by Dmitry
  catalog_data    JSONB,                       -- products/services
  business_rules  JSONB,                       -- custom rules
  bot_id          BIGINT REFERENCES bot_pool(id),
  plan            TEXT DEFAULT 'trial'
                  CHECK (plan IN ('trial', 'starter', 'business', 'premium', 'enterprise')),
  status          TEXT DEFAULT 'trial'
                  CHECK (status IN ('trial', 'active', 'suspended', 'churned')),
  onboarded_at    TIMESTAMPTZ DEFAULT now(),
  manager_telegram_id TEXT,                    -- for future: client's manager alerts
  metadata        JSONB DEFAULT '{}'           -- any extra data
);

CREATE INDEX idx_clients_status ON clients(status);
CREATE INDEX idx_clients_plan ON clients(plan);

-- ─── 3. Plans (reference table) ───
CREATE TABLE IF NOT EXISTS plans (
  id              TEXT PRIMARY KEY,            -- starter, business, premium, enterprise

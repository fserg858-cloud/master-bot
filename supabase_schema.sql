  price_rub       INTEGER NOT NULL,
  requests_month  INTEGER NOT NULL,
  channels        TEXT[] DEFAULT '{"telegram"}',
  features        JSONB DEFAULT '{}',
  is_active       BOOLEAN DEFAULT true
);

